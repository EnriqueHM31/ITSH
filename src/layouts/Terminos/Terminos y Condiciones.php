            Objetivo del Sistema de Justificantes
            El Sistema de Justificantes tiene como objetivo proporcionar a los usuarios una plataforma eficiente y
            transparente para la gestión y validación de ausencias, permisos y otros justificantes académicos o
            administrativos, en cumplimiento de las normativas y políticas internas de la Universidad. <br><br>

