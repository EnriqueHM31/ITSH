            Para poder utilizar el sistema de justificantes, los usuarios deben:
            Registrar una cuenta personal dentro del sistema utilizando sus credenciales institucionales, las cuales
            serán proporcionadas por la Universidad.
            Mantener la confidencialidad de sus datos de acceso. El usuario es responsable de todas las actividades
            realizadas bajo su cuenta, y cualquier uso no autorizado de la misma.
            Actualizar sus datos de contacto e información personal cuando sea necesario.
