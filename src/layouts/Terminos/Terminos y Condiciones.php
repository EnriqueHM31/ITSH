            La decisión sobre la aprobación o rechazo de un justificante será notificada al usuario a través de los
            canales disponibles en el sistema.
            En caso de rechazo, se proporcionará una razón para la decisión y, si corresponde, se ofrecerán alternativas
            o pasos a seguir para presentar nuevamente el justificante. <br><br>


