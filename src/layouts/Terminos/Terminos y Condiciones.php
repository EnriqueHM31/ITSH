            No utilizar el sistema con fines comerciales, maliciosos o fraudulentos, ni para la difusión de contenido
            inapropiado, ofensivo o ilegal. <br><br>

            <h2>Tipos de Justificantes</h2>
            El Sistema de Justificantes puede permitir la presentación de diversos tipos de justificantes, tales como:
            Justificantes médicos: Documentos proporcionados por profesionales de la salud que validan la ausencia por
            razones de salud.
            Justificantes laborales: Documentos que justifican la ausencia por motivos laborales o compromisos
            institucionales.
            Justificantes personales o familiares: Razones personales o familiares que requieren una justificación ante
            la Universidad.
            Cada tipo de justificante podrá requerir diferentes documentos, comprobantes o formatos según la normativa
            interna de la Universidad. <br><br>

            <h2>Validación y Aprobación de Justificantes</h2>
            El sistema está diseñado para permitir que los justificantes sean revisados y validados por los
